function wikidata_identifier(&$triples, $subject, $namespace, $value)
{
	if (0)
	{
		$bnode = '_:' . $namespace;
	}
	else
	{	
		$subject_id = $subject;
		$subject_id = str_replace('<', '', $subject_id);
		$subject_id = str_replace('>', '', $subject_id);
		
		$bnode = '<' . $subject_id . '#'  . $namespace . '>';
	}
	
	$triple = array();
	$triple[] = $subject;
	$triple[] = '<http://schema.org/identifier>';					
	$triple[] = $bnode;
	$triples[] = $triple;
	
	
	switch ($namespace)
	{
		case 'zoobankauthor':
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/propertyID>';					
			$triple[] = '"zoobank"';
	
			$triples[] = $triple;
	
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/value>';

			$triple[] = '"urn:lsid:zoobank.org:author:' . strtoupper($value) . '"';
			$triples[] = $triple;
			break;
	
		case 'zoobankpub':
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/propertyID>';					
			$triple[] = '"zoobank"';
	
			$triples[] = $triple;
	
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/value>';

			$triple[] = '"urn:lsid:zoobank.org:pub:' . strtoupper($value) . '"';
			$triples[] = $triple;
			break;
	
		default:
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/propertyID>';					
			$triple[] = '"' . $namespace . '"';
	
			$triples[] = $triple;
	
			$triple = array();
			$triple[] = $bnode;
			$triple[] = '<http://schema.org/value>';

			$triple[] = '"' . strtolower($value) . '"';
			$triples[] = $triple;
			break;
	}		
	
	
	
	switch ($namespace)
	{
		case 'doi':
			$triple = array();
			$triple[] = $subject;
			$triple[] = '<http://schema.org/sameAs>';					
			$triple[] = '"https://doi.org/' . strtolower($value) . '"';
			
			$triples[] = $triple;
			break;					

		case 'jstor':
			$triple = array();
			$triple[] = $subject;
			$triple[] = '<http://schema.org/sameAs>';					
			$triple[] = '"https://www.jstor.org/stable/' . $value . '"';
			
			$triples[] = $triple;
			break;					


		case 'zoobankpub':
			$triple = array();
			$triple[] = $subject;
			$triple[] = '<http://schema.org/sameAs>';					
			$triple[] = '"urn:lsid:zoobank.org:pub:' . strtoupper($value) . '"';
			
			$triples[] = $triple;
			break;					
			
		default:
			break;
	}
}					

